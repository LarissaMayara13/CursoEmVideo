num1 = input('Digite algo:')
print('O tipo primitivo desse valor é:', type(num1))
print('Só tem espaços?', num1.isspace())
print('É um número?', num1.isnumeric())
print('É alfabético?:', num1.isalpha())
print('É alfanumérico?', num1.isalnum())
print('Está em maiúscula?:', num1.isupper())
print('Está em minúscula?:', num1.islower())
print('Está capitalizada?:',  num1.istitle())



